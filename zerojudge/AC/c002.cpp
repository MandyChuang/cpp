#include <iostream>
using namespace std;

int main()
{
	int n, m;
	while(cin >> n)
	{
		if(n == 0)
			break;
		if(n <= 100)
		{
			m = 91;
		}
		else
			m = n - 10;
		cout << "f91(" << n << ") = " << m << endl;
	}
	return 0;
}

